    private void registerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerBtnActionPerformed
        String username = usernameTf.getText().trim();
        String phoneNumber = phoneNoTf.getText().trim();
        String email = emailTf.getText().trim();
        String password = new String(passwordTf.getPassword()).trim();
        String repassword = new String(repasswordTf.getPassword()).trim();
        
        if(username.isEmpty() || phoneNumber.isEmpty() ||email.isEmpty() || password.isEmpty() || repassword.isEmpty()){
            JOptionPane.showMessageDialog(this, "Please fill in all field!");
        }
        else if(!password.equals(repassword)){
            JOptionPane.showMessageDialog(this, "Password do not match, please enter again!");
        }
        else{
            Random rand = new Random();
            int randNum = rand.nextInt(1000);
            String memberId = "M" + Integer.toString(randNum);
            Member newMember = new Member(memberId, username, phoneNumber, email, password);
            
            MemberList memberList = new MemberList();
            memberList.addMember(newMember);
            MainGUI mainGUI = new MainGUI();
            mainGUI.setMemberId(memberId);
            mainGUI.setVisible(true);
            this.dispose();// close the register form
        }
    }//GEN-LAST:event_registerBtnActionPerformed

